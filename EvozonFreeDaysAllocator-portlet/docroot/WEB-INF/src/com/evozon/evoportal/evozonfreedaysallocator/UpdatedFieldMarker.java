package com.evozon.evoportal.evozonfreedaysallocator;

public class UpdatedFieldMarker {

	private boolean spouse;
	private boolean children;
	private boolean building;
	private boolean phones;
	private boolean skype;
	private boolean idCard;
	private boolean addresses;

	public boolean isSpouse() {
		return spouse;
	}

	public void setSpouse(boolean spouse) {
		this.spouse = spouse;
	}

	public boolean isChildren() {
		return children;
	}

	public void setChildren(boolean children) {
		this.children = children;
	}

	public boolean isBuilding() {
		return building;
	}

	public void setBuilding(boolean building) {
		this.building = building;
	}

	public boolean isPhones() {
		return phones;
	}

	public void setPhones(boolean phones) {
		this.phones = phones;
	}

	public boolean isSkype() {
		return skype;
	}

	public void setSkype(boolean skype) {
		this.skype = skype;
	}

	public boolean isIdCard() {
		return idCard;
	}

	public void setIdCard(boolean idCard) {
		this.idCard = idCard;
	}

	public boolean isAddresses() {
		return addresses;
	}

	public void setAddresses(boolean addresses) {
		this.addresses = addresses;
	}

}
